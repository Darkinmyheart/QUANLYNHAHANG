    private void nccCbbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nccCbbActionPerformed
        // TODO add your handling code here:
        cbbHangNhap.removeAllItems();
        model.setRowCount(0);
        soluongtxt.setText("0");
        if (nccCbb.getSelectedItem() != null) {
            for (NhaCungCap nhaCungCap : dsncc) {
                if (nhaCungCap.getTen().equalsIgnoreCase(nccCbb.getSelectedItem().toString())) {
                    for (Map.Entry<String[], NhaCungCapCT> entry : dsnccct.entrySet()) {
                        if (entry.getKey()[0].equalsIgnoreCase(nhaCungCap.getID())) {
                            cbbHangNhap.addItem(new NguyenLieuDao().getObjectById(entry.getKey()[1]).getTen());
                        }
                    }
                }
            }
        }

    }//GEN-LAST:event_nccCbbActionPerformed

