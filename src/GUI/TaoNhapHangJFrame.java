    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String idncc = "";
        int idnguoinhan = 0;
        for (NhaCungCap nhaCungCap : dsncc) {
            if(nhaCungCap.getTen().equalsIgnoreCase(nccCbb.getSelectedItem().toString())){
                idncc = nhaCungCap.getID();
            }
        }
        for (int i = 0;i<tableDs.getRowCount();i++) {
            for(NguyenLieu nl :dsnl){
                if(nl.getTen().equalsIgnoreCase(tableDs.getValueAt(i, 0).toString())){
                    dsNhaphangCT.add(new NhapHangCT(id, nl.getId(), Integer.parseInt(tableDs.getValueAt(i, 1).toString())));
                }
            }
        }
        for (TaiKhoan tk1 : new TaiKhoanDao().GetArrayListAll()) {
            if(new NhanVienDao().getObjectById(tk1.getIdNhanVien()).getTen().equalsIgnoreCase(cbbNguoiNhan.getSelectedItem().toString())){
                idnguoinhan = tk1.getId();
            }
        }
        int checknhap = new NhapHangDAO().InsertSQL(new NhapHang(id, idncc, tong, jTextField1.getText(), jTextField2.getText(), new Date(), idnguoinhan));
        System.out.println(checknhap);
        if(checknhap ==1){
            System.out.println(dsNhaphangCT.size());
            new NhapHangCTDAO().InsertSQLArraList(dsNhaphangCT);
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            dispose();
        }
        ServerController.Server.testGui.formSanPham1.LoadArrayList("");
    }//GEN-LAST:event_jButton2ActionPerformed

